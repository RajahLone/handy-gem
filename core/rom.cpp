CRom::CRom(char* romfile)
{   mWriteEnable = FALSE;
  strncpy(mFileName, romfile, MAX_PATH);
  Reset();
  
  for (ULONG loop = 0; loop < ROM_SIZE; loop++)
  {   mRomData[loop] = bios[loop];
  }   }

void CRom::Reset(void)
{   // Nothing to do here
}

bool CRom::ContextSave(FILE *fp)
{   if (!fprintf(fp, "CRom::ContextSave")) return 0;
  if (!afwrite(mRomData, sizeof(UBYTE), ROM_SIZE, fp, 0)) return 0;
  
  return 1;
}

bool CRom::ContextLoad(LSS_FILE *fp)
{   char teststr[100] = "XXXXXXXXXXXXXXXXX";
  
  if (!lss_read(teststr, sizeof(char), 17, fp, 0)) return 0;
  if (strcmp(teststr, "CRom::ContextSave") != 0) return 0;
  if (!lss_read(mRomData, sizeof(UBYTE), ROM_SIZE, fp, 0)) return 0;
  
  return 1;
}
